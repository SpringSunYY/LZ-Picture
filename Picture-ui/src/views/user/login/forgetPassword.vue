      message: 'ÂØÜÁ†ÅÈïøÂ∫¶8~20ÂåÖÂê´Â≠óÊØçÂíåÊï∞Â≠óÔºåÂèØ‰ΩøÁî®Á¨¶Âè∑‰ΩÜ‰∏çËÉΩ‰ΩøÁî®Ë°®ÊÉÖ',
      trigger: 'blur',
      validator: (_, value) => {
        if (value.length < 8) {
          return Promise.reject('ÂØÜÁ†ÅÈïøÂ∫¶Ëá≥Â∞ë‰∏∫8‰Ωç');
        }
        // Ê£ÄÊü•ÊòØÂê¶ÂåÖÂê´Ë°®ÊÉÖÁ¨¶Âè∑
        const emojiRegex =
