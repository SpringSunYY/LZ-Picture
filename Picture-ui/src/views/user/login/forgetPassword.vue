  newPassword: [
    { required: true, message: 'ËØ∑ËæìÂÖ•Êñ∞ÂØÜÁ†Å', trigger: 'blur' },
    {
      pattern: "^(?![0-9]+$)(?![a-zA-Z]+$)[0-9A-Za-z]{8,20}$",
